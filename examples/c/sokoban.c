#include <stdio.h>
#include <stdlib.h>
#include <termbox.h>

struct map {
	int width;
	int height;
	char *s;
};

int nmaps;
struct map maps[];

int play(char *map, int w, int h, int level);

int main(int argc, char *argv[])
{
	struct map *p;
	int l;

	l = (argc == 2) ? atoi(argv[1]) : 1;
	if (argc > 2 || l <= 0 || l > nmaps) {
		fprintf(stderr, "usage: sokoban [level]\n");
		return EXIT_FAILURE;
	}
	if (tb_init()) {
		fprintf(stderr, "sokoban: can't open display\n");
		return EXIT_FAILURE;
	}
	for (p = maps + l - 1; p < maps+nmaps; p++)
		switch (play(p->s, p->width, p->height, p - maps + 1)) {
		case 0:
			tb_shutdown();
			return 0;
		case -1:
			tb_shutdown();
			fprintf(stderr, "sokoban: can't allocate memory\n");
			return EXIT_FAILURE;
		}
	tb_shutdown();
	return 0;
}

void draw(char *grid, int w, int h, int level);

int play(char *map, int w, int h, int level)
{
	struct tb_event ev;
	char *grid;
	int s;

	grid = malloc(w * h);
	if (grid == NULL)
		return -1;
	memcpy(grid, map, w * h);
	s = sokoban(grid, w, h, 0);
	while (s == 0 && (draw(grid, w, h, level), tb_poll_event(&ev) > 0)) {
		if (ev.type != TB_EVENT_KEY)
			continue;
		if (ev.key == TB_KEY_CTRL_C)
			break;
		if (ev.ch == 'r')
			memcpy(grid, map, w * h);
		if (ev.key == TB_KEY_ARROW_LEFT)
			s = sokoban(grid, w, h, SOKO_LEFT);
		if (ev.key == TB_KEY_ARROW_UP)
			s = sokoban(grid, w, h, SOKO_UP);
		if (ev.key == TB_KEY_ARROW_DOWN)
			s = sokoban(grid, w, h, SOKO_DOWN);
		if (ev.key == TB_KEY_ARROW_RIGHT)
			s = sokoban(grid, w, h, SOKO_RIGHT);
	}
	free(grid);
	return s;
}

void draw(char *grid, int w, int h, int level)
{
	char s[8192];
	int i, x, y;

	tb_clear();
	for (i = 0; i < w*h; i++) {
		x = tb_width()/2 - w + i%w*2;
		y = tb_height()/2 - (h + 2)/2 + i/w;
		if (grid[i] == 'p' || grid[i] == 'P') {
			tb_change_cell(x+0, y, ' ', 0, TB_RED);
			tb_change_cell(x+1, y, ' ', 0, TB_RED);
		}
		if (grid[i] == '#') {
			tb_change_cell(x+0, y, ' ', 0, TB_BLUE);
			tb_change_cell(x+1, y, ' ', 0, TB_BLUE);
		}
		if (grid[i] == 'c') {
			tb_change_cell(x+0, y, ' ', 0, TB_YELLOW);
			tb_change_cell(x+1, y, ' ', 0, TB_YELLOW);
		}
		if (grid[i] == 'C') {
			tb_change_cell(x+0, y, ' ', 0, TB_GREEN);
			tb_change_cell(x+1, y, ' ', 0, TB_GREEN);
		}
		if (grid[i] == '.') {
			tb_change_cell(x+0, y, '[', TB_GREEN, 0);
			tb_change_cell(x+1, y, ']', TB_GREEN, 0);
		}
	}
	sprintf(s, "Level: %3d", level);
	x = tb_width()/2 - strlen(s)/2;
	y = tb_height()/2 - (h + 2)/2 + h + 1;
	for (i = 0; s[i]; i++)
		tb_change_cell(x+i, y, s[i], 0, 0);
	tb_present();
}

