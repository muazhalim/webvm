#include <string.h>

struct grid {
	char *p;
	int w;
	int h;
};

static int get(struct grid *g, int x, int y)
{
	if (x < 0 || x >= g->w || y < 0 || y >= g->h)
		return '#';
	return g->p[y*g->w + x];
}

static void set(struct grid *g, int x, int y, int c)
{
	if (x >= 0 && x < g->w && y >= 0 && y < g->h)
		g->p[y*g->w + x] = c;
}

static int shift(struct grid *g, int px, int py, int dx, int dy)
{
	int nx = px + dx;
	int ny = py + dy;
	int cx = nx + dx;
	int cy = ny + dy;

	switch (get(g, nx, ny)) {
	case ' ':
	case '.':
		set(g, px, py, (get(g, px, py) == 'p') ? ' ' : '.');
		set(g, nx, ny, (get(g, nx, ny) == ' ') ? 'p' : 'P');
		return 0;
	case 'c':
	case 'C':
		if (strchr(" .", get(g, cx, cy)) != NULL) {
			set(g, px, py, (get(g, px, py) == 'p') ? ' ' : '.');
			set(g, nx, ny, (get(g, nx, ny) == 'c') ? 'p' : 'P');
			set(g, cx, cy, (get(g, cx, cy) == ' ') ? 'c' : 'C');
			return (memchr(g->p, 'c', g->w * g->h) == NULL);
		}
	}
	return 0;
}

int sokoban(char *p, int w, int h, int d)
{
	int incr[][2] = {
		{ 0, 0 }, { 0, -1 }, { 0, +1 }, { -1, 0 }, { +1, 0 }
	};
	struct grid g;
	int i, crates, landings;

	g.p = p;
	g.w = w;
	g.h = h;
	crates = landings = 0;
	for (i = 0; i < w*h; i++) {
		landings += (strchr("PC.", p[i]) != NULL);
		crates += (strchr("cC", p[i]) != NULL);
	}
	if (landings != crates)
		return -1;
	for (i = 0; i < w*h; i++)
		if (p[i] == 'p' || p[i] == 'P')
			return shift(&g, i % w, i / w, incr[d][0], incr[d][1]);
	return -1;
}

